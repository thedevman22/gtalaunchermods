#!/usr/bin/env node
/**
 * Bakes Supabase (and site) URLs into the renderer bundle so installed .exe builds work.
 * Reads from process.env first (CI secrets), then .env on disk.
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const envPath = join(root, '.env')
const outPath = join(root, 'src/renderer/src/config/supabase.build.ts')

function parseEnvFile(text) {
  const vars = {}
  for (const line of text.split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eq = trimmed.indexOf('=')
    if (eq === -1) continue
    const key = trimmed.slice(0, eq).trim()
    let value = trimmed.slice(eq + 1).trim()
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1)
    }
    vars[key] = value
  }
  return vars
}

function escapeTs(value) {
  return value.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
}

function isPlaceholder(url, key) {
  return (
    !url ||
    !key ||
    url.includes('your-project') ||
    key === 'your-anon-key' ||
    key.startsWith('your-')
  )
}

let fileEnv = {}
if (existsSync(envPath)) {
  fileEnv = parseEnvFile(readFileSync(envPath, 'utf8'))
}

const url = process.env.VITE_SUPABASE_URL ?? fileEnv.VITE_SUPABASE_URL ?? ''
const key = process.env.VITE_SUPABASE_ANON_KEY ?? fileEnv.VITE_SUPABASE_ANON_KEY ?? ''
const website = process.env.VITE_MODHARBOR_WEBSITE_URL ?? fileEnv.VITE_MODHARBOR_WEBSITE_URL ?? ''
const billing = process.env.VITE_BILLING_API_URL ?? fileEnv.VITE_BILLING_API_URL ?? ''

const contents = `/** Auto-generated by scripts/sync-supabase-config.mjs — do not edit by hand */
export const BUILD_SUPABASE_URL = '${escapeTs(url)}'
export const BUILD_SUPABASE_ANON_KEY = '${escapeTs(key)}'
export const BUILD_WEBSITE_URL = '${escapeTs(website)}'
export const BUILD_BILLING_API_URL = '${escapeTs(billing)}'
`

if (isPlaceholder(url, key)) {
  console.warn(
    'Supabase config missing or still using .env.example placeholders.\n' +
      'The app will build, but sign-in will not work until you set real values.\n' +
      'Local: edit .env then run `npm run config:sync`.\n' +
      'CI: add VITE_SUPABASE_URL and VITE_SUPABASE_ANON_KEY repository secrets.'
  )
  writeFileSync(outPath, contents)
  console.log('Wrote', outPath, '(empty — auth disabled in this build)')
  process.exit(0)
}

writeFileSync(outPath, contents)
console.log('Wrote', outPath)
